    // Thanks to http://unserializableone.blogspot.com/2009/01/redirecting-systemout-and-systemerr-to.html
    // for the code that went here.
    private void updateTextArea(final String text) {
        SwingUtilities.invokeLater(new Runnable() {
            public void run() {
            consoleArea.append(text);
            }
        });
    }
    private void redirectSystemStreams() {
        OutputStream out = new OutputStream() {
            @Override
            public void write(int b) throws IOException {
                updateTextArea(String.valueOf((char) b));
            }
            @Override
            public void write(byte[] b, int off, int len) throws IOException {
                updateTextArea(new String(b, off, len));
            }
            @Override
            public void write(byte[] b) throws IOException {
                write(b, 0, b.length);
            }
        };
        System.setOut(new PrintStream(out, true));
        System.setErr(new PrintStream(out, true));
    }
    private boolean parseSettings()
    {
        String[] settings;
        try
	{
            BufferedReader input = new BufferedReader( new FileReader( "settings.ini"));
            String line = input.readLine();
            if (line == null )
            {
                return false;
            }
            settings = line.split(" ");
            if (settings.length < 4 )
            {
                return false;
            }
            nameField.setText(settings[0]);
            serverField.setText(settings[1]);
            portField.setText(settings[2]);
            gmField.setText(settings[3]);
            line = input.readLine();
            if ( line == null )
            {
                return false;
            }
            joinField.setText(line);
            return true;
	}
	catch (Exception e)
	{
            return false;
	}
    }
    private boolean saveSettings()
    {
        PrintWriter output = null;
        try
	{
            output = new PrintWriter(new FileOutputStream("settings.ini"));
	}
	catch(FileNotFoundException e)
	{
            return false;
	}
        output.println(nameField.getText()+" "+serverField.getText()+" "+portField.getText()+" "+gmField.getText());
        output.println(joinField.getText());
	output.close();
	return true;
    }
    
