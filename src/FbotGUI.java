        bot.setVerbose(true);
        int port;
        try {
            port = Integer.parseInt(portField.getText());
        } catch (Exception e) {
            port = 6668;
        }

        //speed things up!
        bot.setMessageDelay(1);
        bot.setGm(gmField.getText());
        bot.setJoin(joinField.getText());
        try {
            saveSettings();
            bot.connect(serverField.getText(), port);
        } catch (Exception e) {
            System.out.println("Connection failed.");
        }
